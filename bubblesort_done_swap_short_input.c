// Function to swap two numbers
void swap(short int* a, short int* b) {
    short int temp = *a;
    *a = *b;
    *b = temp;
}

// Bubble sort with doneSwap optimization
void bubbleSortOptimized(short int arr[], int n) {
    bool doneSwap;

    for (int i = 0; i < n - 1; i++) {
        doneSwap = false;

        for (int j = 0; j < n - i - 1; j++) {
            if (arr[j] > arr[j + 1]) {
                swap(&arr[j], &arr[j + 1]);
                doneSwap = true;
            }
        }

        if (!doneSwap)
            break;
    }
}

